package day14_Review;

public class C06_SwitchCase {
    public static void main(String[] args) {

        System.out.println("Başladı");
        int sayi = 1;

        switch (sayi){
            case 1 :
                System.out.println("A");
                break;
            case 2:
                System.out.println("B");
                break;
            default:
                System.out.println("C");



        }

    }
}
