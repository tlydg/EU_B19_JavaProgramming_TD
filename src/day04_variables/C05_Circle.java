package day04_variables;

public class C05_Circle {

    public static void main(String[] args) {

        //declare variables
        //PI, radius (yarıçap), area(alan), perimeter(çevre)

        double PI=3.14;
        double r=8;
        double area=PI*r*r;
        double parimeter= 2*PI*r;

        System.out.println("PI = " + PI); // 3.14
        System.out.println("r = " + r);   // 8
        System.out.println("area = " + area); // 200,96
        System.out.println("parimeter = " + parimeter); // 50,24


    }
}
