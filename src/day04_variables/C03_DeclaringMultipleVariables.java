package day04_variables;

public class C03_DeclaringMultipleVariables {
    public static void main(String[] args) {

        int i1=2;
        int i2=3;

        int i3, i4;
        i3=5;
        i4=6;

        System.out.println("i1 = " + i1); //2
        System.out.println("i2 = " + i2); //3
        System.out.println("i3 = " + i3); //5
        System.out.println("i4 = " + i4); //6


        int i5=7, i6=9;
        System.out.println(i5); //7
        System.out.println("i6 = " + i6); //9

    }
}
